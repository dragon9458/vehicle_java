package Params;


public class VariableParams {
    long Long_One = 120;
    long Long_Two = 160;
    long Width = 70;
    long high = 50;
    long tilt = 80;

    int x = 220;
    int y = 160;
    int height = 320;
    int width = 200;

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth(int width) {
        return width;
    }
}
